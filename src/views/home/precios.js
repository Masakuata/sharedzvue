export function getProductos() {
    let productosFormateados = [];  

    precios.forEach((producto, index) => {
        let productoFormateado = {};
        productoFormateado.nombre = producto.nombre;
        productoFormateado.presentacion = producto.presentacion;
        productoFormateado.raza = 'indefinida';
        productoFormateado.tipoMascota = producto.tipoMascota;
       
        productoFormateado.precios = [];

        let idAutoconsumo = 2
        let idVeterinario = 1

        let precioStringAuto = producto.autoconsumo.replace('$', '');
        precioStringAuto = precioStringAuto.replace(',', '');
        let precioAutoFloat = parseFloat(precioStringAuto);

        let precioStringVet = producto.Veterinario.replace('$', '');
        let precioVetFloat = parseFloat(precioStringVet);

        productoFormateado.precioDefecto = precioAutoFloat;

        productoFormateado.precios.push({
            id: idAutoconsumo,
            precio: precioAutoFloat,
        });
        productoFormateado.precios.push({
            id: idVeterinario,
            precio:precioVetFloat,
        });

        

        productosFormateados.push(productoFormateado);

    });

    return productosFormateados;

}